package Aula_12_06;

public class Atividade_04 {

	public static void main(String[] args) {
		int soma=0, numero=1;

		while (numero <= 15) {

			soma += numero;

			numero++;

			System.out.println("A soma dos numeros é "+ soma); 
		}
		System.out.println("----------------------------"); 
		System.out.println("| A soma dos numeros é "+ soma + " |"); 
		System.out.println("----------------------------");

	}

}
